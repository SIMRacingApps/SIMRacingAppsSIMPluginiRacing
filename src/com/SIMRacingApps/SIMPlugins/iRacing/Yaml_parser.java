    enum yaml_state {
        space,
        key,
        keysep,
        value,
        newline
    };

    public Yaml_parser() {
    }

    private void _storeData(Map<String,Object> m, StringBuilder key, StringBuilder value) {
        
        String s = value.toString().trim();

        try {
            if (s.startsWith("\"") && s.endsWith("\"")) {
                //"1234"
                //012345
                if (s.length() > 2)
                    m.put(key.toString(),s.substring(1, s.length() - 1));
                else
                    m.put(key.toString(),"");
            }
            else
            if (s.startsWith("0x")) {
                Integer i = Integer.parseInt(s.substring(2),16);
                m.put(key.toString(),i);
            }
            else {
                Integer i = Integer.parseInt(s);
                m.put(key.toString(),i);
            }
        }
        catch (NumberFormatException e) {
            try {
                Double d = Double.parseDouble(s);
                m.put(key.toString(),d);
            }
            catch (NumberFormatException e2) {
                m.put(key.toString(),s);
            }
        }
        
    }
