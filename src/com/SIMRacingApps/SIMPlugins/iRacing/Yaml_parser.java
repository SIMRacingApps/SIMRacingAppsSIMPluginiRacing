    public TreeMap<String,Object> load(byte data[]) {
        TreeMap<String,Object> results= new TreeMap<String,Object>();
        ArrayList<Object> collections = new ArrayList<Object>();
        StringBuilder key             = new StringBuilder();
        StringBuilder value           = new StringBuilder();
        yaml_state state              = yaml_state.newline;  //start as a new line
        int prevDepth = -1;
        int depth = 0;

        collections.add(results);
        
        int len = data.length; //optimize this for speed
        for (int i=0; i < len; i++) {

            //convert byte to unsigned char
            char c = (char) ((int)data[i] & 0xFF);

            switch(c)
            {
            case ' ':
                if (state == yaml_state.key) {
                    key.append(c);          //add to existing key
                }
                else
                if (state == yaml_state.value) {        //add to existing value
                    value.append(c);
                }
                else
                if (state == yaml_state.newline || state == yaml_state.space) {
                    depth++;
                    state = yaml_state.space;
                }
                else
                if (state == yaml_state.keysep) {
                    state = yaml_state.value;
                }
                break;
            case '-':
                if (state == yaml_state.key) {
                    key.append(c);          //add to existing key
                }
                else
                if (state == yaml_state.value) {        //add to existing value
                    value.append(c);
                }
                else
                if (state == yaml_state.newline || state == yaml_state.space) {
                    
                    if (key.length() > 0) {  //do we have a key from the previous line
                        
                        //because the hyphen appears at the same depth as the key that defines it
                        //I'm going to increment it to make it unique because there's a space after it
                        depth++;
                        
                        //if this is a new depth level, create a new collection
                        if (depth > prevDepth) { 
                            //create a new map collection and add it to the previous
                            @SuppressWarnings("unchecked")
                            Map<String,Object>            m = (Map<String, Object>) collections.get(collections.size()-1);
                            ArrayList<Map<String,Object>> a = new ArrayList<Map<String,Object>>();
                            Map<String,Object>            n = new TreeMap<String,Object>();
                            
                            m.put(key.toString(), a);   //update previous map with the array
                            collections.add(a);         //track the collection
                            collections.add(n);         //track the collection
                            a.add(n);
                            
                            //TODO: If there's a value here, I don't think it's valid. Throw an error?
                            //      For now just ignore
                            //SessionInfo:
                            // Session: somevalue        <<<<<<<<
                            // - SessionNum: 1
                            //   SessionName: Qual
                            // - SessionNum: 2
                            //   SessionName: Race
                        }
                        else
                        //if this is an existing level, add the value to the collection
                        if (depth <= prevDepth) {
                            //SessionInfo:
                            // Session:
                            // - SessionNum: 1
                            //   SessionLaps: unlimited     <<<<<<<<
                            // - SessionNum: 2
                            //   SessionName: Race
                            @SuppressWarnings("unchecked")
                            Map<String,Object> m = (Map<String, Object>) collections.get(collections.size()-1);
                            _storeData(m,key,value);
                            
                            //if we're at the same level, add a new map to the array
                            if (depth == (prevDepth-1)) {
                                collections.remove(collections.size()-1);   //remove the current map
                                @SuppressWarnings("unchecked")
                                ArrayList<Map<String,Object>> a = (ArrayList<Map<String,Object>>)collections.get(collections.size()-1);
                                Map<String,Object>            n = new TreeMap<String,Object>();
                                a.add(n);           //add new map to array
                                collections.add(n); //add new map to the collections
                            }
                            else {
                                //collections at a higher depth, must be terminated
                                //SessionInfo:
                                // Session:
                                // - SessionNum: 1
                                //   SessionLaps: unlimited
                                // SessionExtra: somevalue      <<<<<<<<
                                while (depth < prevDepth) {
                                    if (collections.size() > 1)
                                        collections.remove(collections.size()-1);
                                    prevDepth--;
                                }
                                @SuppressWarnings("unchecked")
                                ArrayList<Map<String,Object>> a = (ArrayList<Map<String,Object>>)collections.get(collections.size()-1);
                                Map<String,Object>            n = new TreeMap<String,Object>();
                                a.add(n);           //add new map to array
                                collections.add(n); //add new map to the collections
                            }
                        }
                    }
                    
                    key   = new StringBuilder();  //start a new key
                    value = new StringBuilder();  //start a new value
                }
                break;
            case ':':
                if (state == yaml_state.key) {
                    //key is found, but hold that thought. Need to see what's next
                    state = yaml_state.keysep;
                }
                else
                if (state == yaml_state.value) {
                    value.append(c);
                }
                break;
            case '\n':
            case '\r':
                if(state != yaml_state.newline)
                {
                    state = yaml_state.newline;
                    prevDepth = depth;
                    depth = 0;
                }
                break;
            default:
                if (state == yaml_state.key) {
                    key.append(c);                //add to existing key
                }
                else
                if (state == yaml_state.value) {        //add to existing value
                    value.append(c);
                }
                else
                if (state == yaml_state.newline || state == yaml_state.space) {
                    
                    if (key.length() > 0) {  //do we have a key from the previous line
                        
                        //if this is a new depth level, create a new collection
                        if (depth > prevDepth) { 
                            //create a new map collection and add it to the previous
                            @SuppressWarnings("unchecked")
                            Map<String,Object> m = (Map<String, Object>) collections.get(collections.size()-1);
                            Map<String,Object> n = new TreeMap<String,Object>();
                            m.put(key.toString(), n);
                            collections.add(n);   //track the collection
                            
                            //TODO: If there's a value here, I don't think it's valid. Throw an error?
                            //      For now just ignore
                            //SessionInfo: somevalue        <<<<<<<<
                            // Session:
                            // - SessionNum: 1
                        }
                        else
                        //if this is an existing level, add the value to the collection
                        if (depth <= prevDepth) {
                            //SessionInfo:
                            // Session:
                            // - SessionNum: 1
                            //   SessionLaps: unlimited     <<<<<<<<
                            @SuppressWarnings("unchecked")
                            Map<String,Object> m = (Map<String, Object>) collections.get(collections.size()-1);
                            _storeData(m,key,value);

                            //collections at a higher depth, must be terminated
                            //SessionInfo:
                            // Session:
                            // - SessionNum: 1
                            //   SessionLaps: unlimited
                            // SessionExtra: somevalue      <<<<<<<<
                            while (depth < prevDepth) {
                                if (collections.size() > 1)
                                    collections.remove(collections.size()-1);
                                prevDepth--;
                            }
                        }
                    }
                    
                    key = new StringBuilder();          //start a new key
                    key.append(c);
                    value = new StringBuilder();        //start a new value
                    state = yaml_state.key;
                }
                break;
            }
        }
        return results;
    }

